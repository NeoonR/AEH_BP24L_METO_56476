package pl.pp;

public class mySecondApp {
    public static void main(String[] args) {
        // Step 1: Declare an integer variable x and assign it a value of 10
        int x = 10;

        // Step 2: Calculate the double and square of x
        int double_X = 2 * x;
        int square_X = x * x;

        // Step 3: Display the results
        System.out.println("The original number x is: " + x);
        System.out.println("The double of x is: " + double_X);
        System.out.println("The square of x is: " + square_X);

    }
}
